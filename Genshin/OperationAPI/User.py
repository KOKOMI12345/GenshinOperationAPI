class Keyboard:
    """
    NOTE : 键盘操作类
    """
    ENTER = 0x0D
    ESC = 0x1B
    BACKSPACE = 0x08
    TAB = 0x09
    SHIFT = 0x10
    W = 0x57
    A = 0x41
    S = 0x53
    D = 0x44
    E = 0x45
    Q = 0x51
    F = 0x46
    SPACE = 0x20

